# 20. Присвоїти цілій змінній d першу цифру з дробової частини позитивного дійсного числа x.


def number(a):
    if a > 0:
        result = int(a * 10) % 10
        return result
    else:
        print("Помилка")


b = float(input("Введіть дробове позитивне число: "))
d = number(b)
print(d)
